public class RGBColor {
    public final double R;
    public final double G;
    public final double B;
    public final double opacity;

    public RGBColor(double R, double G, double B, double opacity) {
        this.R = R;
        this.G = G;
        this.B = B;
        this.opacity = opacity;
    }
    
}
